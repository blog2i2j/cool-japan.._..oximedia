#![allow(dead_code)]
//! Linked clip groups for synchronized multi-track editing.
//!
//! When clips across different tracks represent the same moment in time
//! (e.g. video and its corresponding audio), they should move together.
//! A [`LinkGroup`] binds a set of `(TrackId, ClipId)` pairs so that any
//! positional change applied to one member is automatically propagated to
//! all other members in the group.
//!
//! The [`LinkManager`] maintains all link groups for a timeline and provides
//! methods to create, dissolve, and query groups. When a clip is moved, the
//! caller asks the manager for the `move_instructions` that keep the group
//! in sync.

use std::collections::HashMap;

use serde::{Deserialize, Serialize};
use uuid::Uuid;

use crate::clip::ClipId;
use crate::track::TrackId;

/// Unique identifier for a link group.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct LinkGroupId(Uuid);

impl LinkGroupId {
    /// Creates a new random link-group ID.
    #[must_use]
    pub fn new() -> Self {
        Self(Uuid::new_v4())
    }

    /// Returns the underlying UUID.
    #[must_use]
    pub const fn as_uuid(&self) -> &Uuid {
        &self.0
    }
}

impl Default for LinkGroupId {
    fn default() -> Self {
        Self::new()
    }
}

impl std::fmt::Display for LinkGroupId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.0)
    }
}

/// A single member of a link group.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct LinkMember {
    /// Track that contains the clip.
    pub track_id: TrackId,
    /// Clip that is part of the group.
    pub clip_id: ClipId,
}

impl LinkMember {
    /// Create a new link member.
    #[must_use]
    pub const fn new(track_id: TrackId, clip_id: ClipId) -> Self {
        Self { track_id, clip_id }
    }
}

/// A group of linked clips that move together.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct LinkGroup {
    /// Unique ID for this group.
    pub id: LinkGroupId,
    /// Human-readable label (optional).
    pub label: String,
    /// Member clips.
    members: Vec<LinkMember>,
    /// Whether the group is currently active (locked together).
    pub enabled: bool,
}

impl LinkGroup {
    /// Create a new, empty link group.
    #[must_use]
    pub fn new(label: impl Into<String>) -> Self {
        Self {
            id: LinkGroupId::new(),
            label: label.into(),
            members: Vec::new(),
            enabled: true,
        }
    }

    /// Create a link group from a list of members.
    #[must_use]
    pub fn from_members(label: impl Into<String>, members: Vec<LinkMember>) -> Self {
        Self {
            id: LinkGroupId::new(),
            label: label.into(),
            members,
            enabled: true,
        }
    }

    /// Add a member to the group.
    ///
    /// Does nothing if the member is already present.
    pub fn add_member(&mut self, member: LinkMember) {
        if !self.members.contains(&member) {
            self.members.push(member);
        }
    }

    /// Remove a member from the group.
    ///
    /// Returns `true` if the member was found and removed.
    pub fn remove_member(&mut self, member: &LinkMember) -> bool {
        let before = self.members.len();
        self.members.retain(|m| m != member);
        self.members.len() < before
    }

    /// Returns `true` if the given clip is a member.
    #[must_use]
    pub fn contains(&self, track_id: TrackId, clip_id: ClipId) -> bool {
        self.members
            .iter()
            .any(|m| m.track_id == track_id && m.clip_id == clip_id)
    }

    /// Returns the member list.
    #[must_use]
    pub fn members(&self) -> &[LinkMember] {
        &self.members
    }

    /// Number of members.
    #[must_use]
    pub fn member_count(&self) -> usize {
        self.members.len()
    }

    /// Returns `true` if the group has fewer than 2 members (no linking effect).
    #[must_use]
    pub fn is_trivial(&self) -> bool {
        self.members.len() < 2
    }
}

/// A move instruction generated by the link manager.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct LinkedMoveInstruction {
    /// Track of the clip to move.
    pub track_id: TrackId,
    /// Clip to move.
    pub clip_id: ClipId,
    /// Frame delta (positive = later, negative = earlier).
    pub delta: i64,
}

/// Manages all link groups for a timeline.
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct LinkManager {
    groups: Vec<LinkGroup>,
    /// Reverse index: (TrackId, ClipId) → LinkGroupId for fast lookup.
    #[serde(skip)]
    index: HashMap<(TrackId, ClipId), LinkGroupId>,
}

impl LinkManager {
    /// Create a new, empty link manager.
    #[must_use]
    pub fn new() -> Self {
        Self::default()
    }

    /// Rebuild the reverse index from the current groups list.
    pub fn rebuild_index(&mut self) {
        self.index.clear();
        for group in &self.groups {
            for member in &group.members {
                self.index
                    .insert((member.track_id, member.clip_id), group.id);
            }
        }
    }

    /// Create a new link group and return its ID.
    pub fn create_group(&mut self, label: impl Into<String>) -> LinkGroupId {
        let group = LinkGroup::new(label);
        let id = group.id;
        self.groups.push(group);
        id
    }

    /// Create a link group pre-populated with members.
    pub fn create_group_with_members(
        &mut self,
        label: impl Into<String>,
        members: Vec<LinkMember>,
    ) -> LinkGroupId {
        let group = LinkGroup::from_members(label, members.clone());
        let id = group.id;
        for m in &members {
            self.index.insert((m.track_id, m.clip_id), id);
        }
        self.groups.push(group);
        id
    }

    /// Add a member to an existing group.
    ///
    /// Returns `true` if the group was found and the member was added.
    pub fn add_to_group(
        &mut self,
        group_id: LinkGroupId,
        member: LinkMember,
    ) -> bool {
        if let Some(group) = self.groups.iter_mut().find(|g| g.id == group_id) {
            group.add_member(member);
            self.index.insert((member.track_id, member.clip_id), group_id);
            true
        } else {
            false
        }
    }

    /// Remove a member from its group.
    ///
    /// If the group becomes trivial (< 2 members), it is dissolved automatically.
    /// Returns `true` if the member was found in a group.
    pub fn remove_member(&mut self, track_id: TrackId, clip_id: ClipId) -> bool {
        let member = LinkMember::new(track_id, clip_id);
        let group_id = self.index.remove(&(track_id, clip_id));
        if let Some(gid) = group_id {
            if let Some(group) = self.groups.iter_mut().find(|g| g.id == gid) {
                group.remove_member(&member);
                if group.is_trivial() {
                    // Dissolve trivial groups.
                    let remaining: Vec<_> = group.members.iter().copied().collect();
                    for m in &remaining {
                        self.index.remove(&(m.track_id, m.clip_id));
                    }
                    self.groups.retain(|g| g.id != gid);
                }
            }
            true
        } else {
            false
        }
    }

    /// Dissolve a group entirely.
    ///
    /// Returns `true` if the group existed.
    pub fn dissolve_group(&mut self, group_id: LinkGroupId) -> bool {
        if let Some(pos) = self.groups.iter().position(|g| g.id == group_id) {
            let group = &self.groups[pos];
            for m in &group.members {
                self.index.remove(&(m.track_id, m.clip_id));
            }
            self.groups.remove(pos);
            true
        } else {
            false
        }
    }

    /// Returns the group ID for a clip, if any.
    #[must_use]
    pub fn group_for(&self, track_id: TrackId, clip_id: ClipId) -> Option<LinkGroupId> {
        self.index.get(&(track_id, clip_id)).copied()
    }

    /// Returns all groups.
    #[must_use]
    pub fn groups(&self) -> &[LinkGroup] {
        &self.groups
    }

    /// Number of groups.
    #[must_use]
    pub fn group_count(&self) -> usize {
        self.groups.len()
    }

    /// Look up the group by ID.
    #[must_use]
    pub fn get_group(&self, group_id: LinkGroupId) -> Option<&LinkGroup> {
        self.groups.iter().find(|g| g.id == group_id)
    }

    /// Given that `(source_track, source_clip)` is about to be moved by `delta`
    /// frames, returns the move instructions for all *other* members in the same
    /// link group (excluding the source itself).
    ///
    /// If the clip is not in any group or the group is disabled, returns an empty
    /// vector.
    #[must_use]
    pub fn move_instructions(
        &self,
        source_track: TrackId,
        source_clip: ClipId,
        delta: i64,
    ) -> Vec<LinkedMoveInstruction> {
        let group_id = match self.index.get(&(source_track, source_clip)) {
            Some(id) => *id,
            None => return Vec::new(),
        };

        let group = match self.groups.iter().find(|g| g.id == group_id) {
            Some(g) => g,
            None => return Vec::new(),
        };

        if !group.enabled {
            return Vec::new();
        }

        group
            .members
            .iter()
            .filter(|m| !(m.track_id == source_track && m.clip_id == source_clip))
            .map(|m| LinkedMoveInstruction {
                track_id: m.track_id,
                clip_id: m.clip_id,
                delta,
            })
            .collect()
    }

    /// Returns `true` if two clips are in the same link group.
    #[must_use]
    pub fn are_linked(
        &self,
        t1: TrackId,
        c1: ClipId,
        t2: TrackId,
        c2: ClipId,
    ) -> bool {
        match (
            self.index.get(&(t1, c1)),
            self.index.get(&(t2, c2)),
        ) {
            (Some(g1), Some(g2)) => g1 == g2,
            _ => false,
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    fn make_member() -> LinkMember {
        LinkMember::new(TrackId::new(), ClipId::new())
    }

    #[test]
    fn test_link_group_creation() {
        let group = LinkGroup::new("AV Pair");
        assert_eq!(group.label, "AV Pair");
        assert!(group.is_trivial());
        assert!(group.enabled);
    }

    #[test]
    fn test_link_group_add_remove_member() {
        let mut group = LinkGroup::new("test");
        let m1 = make_member();
        let m2 = make_member();
        group.add_member(m1);
        group.add_member(m2);
        assert_eq!(group.member_count(), 2);
        assert!(!group.is_trivial());

        // Duplicate add is a no-op.
        group.add_member(m1);
        assert_eq!(group.member_count(), 2);

        assert!(group.remove_member(&m1));
        assert_eq!(group.member_count(), 1);
        assert!(!group.contains(m1.track_id, m1.clip_id));
    }

    #[test]
    fn test_link_group_contains() {
        let mut group = LinkGroup::new("test");
        let m = make_member();
        assert!(!group.contains(m.track_id, m.clip_id));
        group.add_member(m);
        assert!(group.contains(m.track_id, m.clip_id));
    }

    #[test]
    fn test_link_manager_create_group() {
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group("pair1");
        assert_eq!(mgr.group_count(), 1);
        assert!(mgr.get_group(gid).is_some());
    }

    #[test]
    fn test_link_manager_add_to_group() {
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group("pair1");
        let m = make_member();
        assert!(mgr.add_to_group(gid, m));
        assert_eq!(mgr.group_for(m.track_id, m.clip_id), Some(gid));
    }

    #[test]
    fn test_link_manager_create_with_members() {
        let m1 = make_member();
        let m2 = make_member();
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group_with_members("av", vec![m1, m2]);
        assert!(mgr.are_linked(m1.track_id, m1.clip_id, m2.track_id, m2.clip_id));
        assert_eq!(mgr.group_for(m1.track_id, m1.clip_id), Some(gid));
    }

    #[test]
    fn test_link_manager_move_instructions() {
        let m1 = make_member();
        let m2 = make_member();
        let m3 = make_member();
        let mut mgr = LinkManager::new();
        mgr.create_group_with_members("trio", vec![m1, m2, m3]);

        let instrs = mgr.move_instructions(m1.track_id, m1.clip_id, 10);
        assert_eq!(instrs.len(), 2);
        // All deltas should be 10.
        for instr in &instrs {
            assert_eq!(instr.delta, 10);
        }
        // Source clip should not be in the instructions.
        assert!(!instrs
            .iter()
            .any(|i| i.track_id == m1.track_id && i.clip_id == m1.clip_id));
    }

    #[test]
    fn test_link_manager_move_instructions_disabled_group() {
        let m1 = make_member();
        let m2 = make_member();
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group_with_members("pair", vec![m1, m2]);
        // Disable the group.
        if let Some(g) = mgr.groups.iter_mut().find(|g| g.id == gid) {
            g.enabled = false;
        }
        let instrs = mgr.move_instructions(m1.track_id, m1.clip_id, 5);
        assert!(instrs.is_empty());
    }

    #[test]
    fn test_link_manager_move_instructions_unlinked_clip() {
        let mgr = LinkManager::new();
        let instrs = mgr.move_instructions(TrackId::new(), ClipId::new(), 10);
        assert!(instrs.is_empty());
    }

    #[test]
    fn test_link_manager_dissolve_group() {
        let m1 = make_member();
        let m2 = make_member();
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group_with_members("pair", vec![m1, m2]);
        assert!(mgr.dissolve_group(gid));
        assert_eq!(mgr.group_count(), 0);
        assert!(mgr.group_for(m1.track_id, m1.clip_id).is_none());
    }

    #[test]
    fn test_link_manager_dissolve_nonexistent() {
        let mut mgr = LinkManager::new();
        assert!(!mgr.dissolve_group(LinkGroupId::new()));
    }

    #[test]
    fn test_link_manager_remove_member_dissolves_trivial() {
        let m1 = make_member();
        let m2 = make_member();
        let mut mgr = LinkManager::new();
        mgr.create_group_with_members("pair", vec![m1, m2]);
        assert_eq!(mgr.group_count(), 1);

        // Removing one of two members makes the group trivial → auto-dissolve.
        mgr.remove_member(m1.track_id, m1.clip_id);
        assert_eq!(mgr.group_count(), 0);
    }

    #[test]
    fn test_link_manager_remove_unlinked_member() {
        let mut mgr = LinkManager::new();
        assert!(!mgr.remove_member(TrackId::new(), ClipId::new()));
    }

    #[test]
    fn test_link_manager_are_linked_different_groups() {
        let m1 = make_member();
        let m2 = make_member();
        let m3 = make_member();
        let mut mgr = LinkManager::new();
        mgr.create_group_with_members("g1", vec![m1, m2]);
        mgr.create_group_with_members("g2", vec![m3]);

        assert!(mgr.are_linked(m1.track_id, m1.clip_id, m2.track_id, m2.clip_id));
        assert!(!mgr.are_linked(m1.track_id, m1.clip_id, m3.track_id, m3.clip_id));
    }

    #[test]
    fn test_link_manager_rebuild_index() {
        let m1 = make_member();
        let m2 = make_member();
        let mut mgr = LinkManager::new();
        let gid = mgr.create_group_with_members("pair", vec![m1, m2]);
        // Clear the index to simulate deserialization.
        mgr.index.clear();
        assert!(mgr.group_for(m1.track_id, m1.clip_id).is_none());
        mgr.rebuild_index();
        assert_eq!(mgr.group_for(m1.track_id, m1.clip_id), Some(gid));
    }

    #[test]
    fn test_link_group_from_members() {
        let m1 = make_member();
        let m2 = make_member();
        let group = LinkGroup::from_members("test", vec![m1, m2]);
        assert_eq!(group.member_count(), 2);
        assert!(!group.is_trivial());
    }

    #[test]
    fn test_link_group_id_display() {
        let id = LinkGroupId::new();
        let s = format!("{id}");
        assert!(!s.is_empty());
    }
}
